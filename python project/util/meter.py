class AverageMeter(object):
    """
    use for calculate loss, acc
    (loss, accuracy 평균값을 계산하는데 사용)
    """
    def __init__(self):
        self.reset()

    def reset(self):
        self.val = 0
        self.avg = 0
        self.sum = 0
        self.count = 0

    def update(self, val, n=1):
        self.val = val
        self.sum += val * n
        self.count += n
        self.avg = self.sum / self.count